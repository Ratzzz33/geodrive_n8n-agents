-- =============================================
--
-- ЦЕЛИ:
-- 1. Установить часовой пояс 'Asia/Tbilisi' для текущего пользователя БД (neondb_owner)
-- 2. Это гарантирует, что все сессии (n8n, скрипты, API) будут использовать этот часовой пояс
--    по умолчанию, если не переопределено явно.
--
-- =============================================

-- Устанавливаем часовой пояс для роли (пользователя)
ALTER ROLE neondb_owner SET TimeZone TO 'Asia/Tbilisi';

-- На всякий случай устанавливаем и для базы данных
ALTER DATABASE neondb SET TimeZone TO 'Asia/Tbilisi';

-- Проверка текущих настроек (в логах)
DO $$
BEGIN
  RAISE NOTICE 'Часовой пояс установлен на Asia/Tbilisi для роли и БД';
END $$;

